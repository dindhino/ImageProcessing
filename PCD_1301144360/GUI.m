x = get(handles.popupmenu1,'Value');
switch x
    case 1
        imshow(G);
        figure, imshow(G);
        figure, imshow(zoomIn(G,2));
    case 2
        imshow(G);
        figure, imshow(G);
        figure, imshow(zoomIn(G,3));
    case 3
        imshow(G);
        figure, imshow(G);
        figure, imshow(zoomIn(G,4));
